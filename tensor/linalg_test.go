func TestGolubKahan(t *testing.T) {
	t.Parallel()
	tests := []struct {
		a *Dense
		b *Dense
	}{
		{
			a: T2([][]complex64{
				{4, 3, 0, 2, 5},
				{2, 1, 2, 1, 6},
				{4, 4, 0, 3, 0},
				{5, 6, 1, 3, 7},
			}),
			b: T2([][]complex64{
				{-7.81025, 12.50508, 0, 0, 0},
				{0, 3.91667, 4.54233, 0, 0},
				{0, 0, -2.341146, -0.827086, 0},
				{0, 0, 0, 0.112992, 1.21327},
			}),
		},
		{
			a: T2([][]complex64{{1, -2}, {0, 1 + 1i}, {-1, 4}, {-1i, 3}}),
			b: T2([][]complex64{
				{-1.7320504, 3.464101 - 1.7320507i},
				{0, -2.8284276 - 2.8284278i},
				{0, 0},
				{0, 0},
			}),
		},
		{
			a: T2([][]complex64{
				{1, 0, -1, -1i},
				{-2, 1 + 1i, 4, 3},
			}),
			b: T2([][]complex64{
				{-2.2360678, -3.5496478 - 3.5496478i, 0, 0},
				{0, -1.4931061 - 0.6479519i, 0.83887935 - 0.6686367i, 0},
			}),
		},
		{
			a: T2([][]complex64{
				{1, 0, -1, -1i, 3},
				{-2, 1 + 1i, 4, 3, 2},
			}),
			b: T2([][]complex64{
				{-2.2360678, -3.5637052 - 3.5637052i, 0, 0, 0},
				{0, -1.7117013 - 0.86988115i, 3.13104 - 1.7635111i, 0, 0},
			}),
		},
	}
	gk := NewGolubKahan()
	for i, test := range tests {
		t.Run(fmt.Sprintf("%d", i), func(t *testing.T) {
			u, b, v := gk.Solve(test.a)

			// Check u and v are unitary.
			uu := Zeros(1)
			MatMul(uu, u, u.H())
			if err := equal2(uu, Zeros(1).Eye(test.a.Shape()[0], 0).ToSlice2(), 1e-6); err != nil {
				t.Fatalf("%+v", err)
			}
			vv := Zeros(1)
			MatMul(vv, v, v.H())
			if err := equal2(vv, Zeros(1).Eye(test.a.Shape()[1], 0).ToSlice2(), 1e-6); err != nil {
				t.Fatalf("%+v", err)
			}

			// Check u @ b @ v.H == a.
			ubvh, buf := Zeros(1), Zeros(1)
			MatMul(ubvh, u, MatMul(buf, b, v.H()))
			if err := equal2(ubvh, test.a.ToSlice2(), 5e-6); err != nil {
				t.Fatalf("%+v", err)
			}

			// Check b.
			if err := equal2(b, test.b.ToSlice2(), 1e-5); err != nil {
				t.Fatalf("%+v", err)
			}
		})
	}
}
