func equal(a, b *Tensor, tol float32) error {
	if len(a.Shape) != len(b.Shape) {
		return errors.Errorf("%d %d", len(a.Shape), len(b.Shape))
	}
	for i := range a.Shape {
		if a.Shape[i] != b.Shape[i] {
			return errors.Errorf("%d", i)
		}
	}
	if len(a.Data) != len(b.Data) {
		return errors.Errorf("%d %d", len(a.Data), len(b.Data))
	}
	for i := range a.Data {
		diff := cAbs(a.Data[i] - b.Data[i])
		if diff > tol {
			return errors.Errorf("%d %f", i, diff)
		}
	}
	return nil
}

